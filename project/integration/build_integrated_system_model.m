function build_integrated_system_model()
% Build a fully populated integrated PCS Simulink model for R2024b.
% Each subsystem contains functional MATLAB Function blocks implementing
% the actual control/plant algorithms rather than empty placeholders.

model_name = 'integrated_energy_system';
if bdIsLoaded(model_name)
    close_system(model_name, 0);
end
new_system(model_name);
open_system(model_name);

%% Simulation parameters
set_param(model_name, 'StopTime', '0.5');
set_param(model_name, 'Solver', 'FixedStepDiscrete');
set_param(model_name, 'FixedStep', '1e-4');
set_param(model_name, 'SimulationMode', 'normal');

%% ===== Top-level blocks =====
add_block('simulink/Ports & Subsystems/Subsystem', [model_name '/Grid_Source'], ...
    'Position', [50 140 160 200]);
add_block('simulink/Ports & Subsystems/Subsystem', [model_name '/PCC_Breaker'], ...
    'Position', [210 130 320 210]);
add_block('simulink/Ports & Subsystems/Subsystem', [model_name '/LCL_Filter'], ...
    'Position', [370 130 480 210]);
add_block('simulink/Ports & Subsystems/Subsystem', [model_name '/AFE_3Level'], ...
    'Position', [530 120 680 220]);
add_block('simulink/Ports & Subsystems/Subsystem', [model_name '/DC_Bus'], ...
    'Position', [730 110 860 230]);
add_block('simulink/Ports & Subsystems/Subsystem', [model_name '/BiDir_DCDC'], ...
    'Position', [910 80 1060 180]);
add_block('simulink/Ports & Subsystems/Subsystem', [model_name '/Battery_Pack'], ...
    'Position', [1110 90 1220 170]);
add_block('simulink/Ports & Subsystems/Subsystem', [model_name '/DCAC_Inverter'], ...
    'Position', [910 260 1060 360]);
add_block('simulink/Ports & Subsystems/Subsystem', [model_name '/Island_Load'], ...
    'Position', [1110 270 1220 350]);
add_block('simulink/Ports & Subsystems/Subsystem', [model_name '/Controller'], ...
    'Position', [370 380 860 580]);
add_block('simulink/Discrete/Unit Delay', [model_name '/DCDC_Duty_Z1'], ...
    'Position', [885 145 915 175]);
set_param([model_name '/DCDC_Duty_Z1'], 'SampleTime', '1e-4', 'InitialCondition', '0');

% Scopes
add_block('simulink/Sinks/Scope', [model_name '/Scope_Vdc'], 'Position', [920 350 980 380]);
add_block('simulink/Sinks/Scope', [model_name '/Scope_Iabc'], 'Position', [920 390 980 420]);
add_block('simulink/Sinks/Scope', [model_name '/Scope_Batt'], 'Position', [1150 190 1210 220]);

%% ===== Populate all subsystems =====
populate_grid_source(model_name);
populate_pcc_breaker(model_name);
populate_lcl_filter(model_name);
populate_afe(model_name);
populate_dc_bus(model_name);
populate_dcdc(model_name);
populate_battery(model_name);
populate_inverter(model_name);
populate_island_load(model_name);
populate_controller(model_name);

%% ===== Top-level wiring =====
% Power path: Grid -> PCC -> LCL -> AFE -> DC_Bus
add_line(model_name, 'Grid_Source/1', 'PCC_Breaker/1', 'autorouting', 'on');
add_line(model_name, 'PCC_Breaker/1', 'LCL_Filter/1', 'autorouting', 'on');
add_line(model_name, 'LCL_Filter/1', 'AFE_3Level/1', 'autorouting', 'on');
add_line(model_name, 'AFE_3Level/1', 'DC_Bus/1', 'autorouting', 'on');
% DC_Bus -> DCDC -> Battery
add_line(model_name, 'DC_Bus/1', 'BiDir_DCDC/1', 'autorouting', 'on');
add_line(model_name, 'BiDir_DCDC/1', 'Battery_Pack/1', 'autorouting', 'on');
% DC_Bus -> Inverter -> Load
add_line(model_name, 'DC_Bus/1', 'DCAC_Inverter/1', 'autorouting', 'on');
add_line(model_name, 'DCAC_Inverter/1', 'Island_Load/1', 'autorouting', 'on');

% Measurement feedback to controller
add_line(model_name, 'Grid_Source/1', 'Controller/1', 'autorouting', 'on');
add_line(model_name, 'LCL_Filter/2', 'Controller/2', 'autorouting', 'on');
add_line(model_name, 'DC_Bus/2', 'Controller/3', 'autorouting', 'on');
add_line(model_name, 'Battery_Pack/1', 'Controller/4', 'autorouting', 'on');

% Controller commands to actuators
add_line(model_name, 'Controller/1', 'PCC_Breaker/2', 'autorouting', 'on');
add_line(model_name, 'Controller/2', 'AFE_3Level/2', 'autorouting', 'on');
add_line(model_name, 'Controller/3', 'DCDC_Duty_Z1/1', 'autorouting', 'on');
add_line(model_name, 'DCDC_Duty_Z1/1', 'BiDir_DCDC/2', 'autorouting', 'on');
add_line(model_name, 'Controller/4', 'DCAC_Inverter/2', 'autorouting', 'on');

% Scope connections
add_line(model_name, 'DC_Bus/2', 'Scope_Vdc/1', 'autorouting', 'on');
add_line(model_name, 'LCL_Filter/2', 'Scope_Iabc/1', 'autorouting', 'on');
add_line(model_name, 'Battery_Pack/1', 'Scope_Batt/1', 'autorouting', 'on');

%% Save
save_system(model_name);
fprintf('Integrated PCS model "%s" built with populated subsystems.\n', model_name);
end

%% ========== Subsystem population functions ==========

function populate_grid_source(model_name)
sys = [model_name '/Grid_Source'];
try delete_line(sys, 'In1/1', 'Out1/1'); catch, end
try delete_block([sys '/In1']); catch, end
set_param([sys '/Out1'], 'Name', 'Vabc', 'Position', [300 40 330 60]);

add_block('simulink/User-Defined Functions/MATLAB Function', [sys '/GridGen'], ...
    'Position', [80 30 230 80]);
set_ml_fcn(sys, 'GridGen', [...
    'function Vabc = fcn()' newline ...
    '%#codegen' newline ...
    'persistent t' newline ...
    'if isempty(t), t = 0; end' newline ...
    'Ts = 1e-4; f = 50; Vm = 311;' newline ...
    'w = 2*pi*f;' newline ...
    'va = Vm*cos(w*t);' newline ...
    'vb = Vm*cos(w*t - 2*pi/3);' newline ...
    'vc = Vm*cos(w*t + 2*pi/3);' newline ...
    'Vabc = [va; vb; vc];' newline ...
    't = t + Ts;' newline ...
    'end']);
add_line(sys, 'GridGen/1', 'Vabc/1', 'autorouting', 'on');
end

function populate_pcc_breaker(model_name)
sys = [model_name '/PCC_Breaker'];
try delete_line(sys, 'In1/1', 'Out1/1'); catch, end
try delete_block([sys '/In1']); catch, end
try delete_block([sys '/Out1']); catch, end

add_block('simulink/Ports & Subsystems/In1', [sys '/Vabc_in'], 'Position', [30 40 60 60]);
add_block('simulink/Ports & Subsystems/In1', [sys '/PCC_cmd'], 'Position', [30 100 60 120]);
add_block('simulink/Ports & Subsystems/Out1', [sys '/Vabc_out'], 'Position', [300 55 330 75]);

add_block('simulink/User-Defined Functions/MATLAB Function', [sys '/Breaker'], ...
    'Position', [120 40 250 120]);
set_ml_fcn(sys, 'Breaker', [...
    'function Vout = fcn(Vin, cmd)' newline ...
    '%#codegen' newline ...
    '% PCC breaker: passes voltage when cmd >= 0.5' newline ...
    'if cmd >= 0.5' newline ...
    '    Vout = Vin;' newline ...
    'else' newline ...
    '    Vout = zeros(size(Vin));' newline ...
    'end' newline ...
    'end']);
add_line(sys, 'Vabc_in/1', 'Breaker/1', 'autorouting', 'on');
add_line(sys, 'PCC_cmd/1', 'Breaker/2', 'autorouting', 'on');
add_line(sys, 'Breaker/1', 'Vabc_out/1', 'autorouting', 'on');
end

function populate_lcl_filter(model_name)
sys = [model_name '/LCL_Filter'];
try delete_line(sys, 'In1/1', 'Out1/1'); catch, end
try delete_block([sys '/In1']); catch, end
try delete_block([sys '/Out1']); catch, end

add_block('simulink/Ports & Subsystems/In1', [sys '/Vin'], 'Position', [30 40 60 60]);
add_block('simulink/Ports & Subsystems/Out1', [sys '/Vout'], 'Position', [380 40 410 60]);
add_block('simulink/Ports & Subsystems/Out1', [sys '/Iabc'], 'Position', [380 90 410 110]);

add_block('simulink/User-Defined Functions/MATLAB Function', [sys '/LCL'], ...
    'Position', [120 30 320 110]);
set_ml_fcn(sys, 'LCL', [...
    'function [Vout, Iabc] = fcn(Vin)' newline ...
    '%#codegen' newline ...
    'persistent iL vC' newline ...
    'if isempty(iL), iL = zeros(3,1); vC = zeros(3,1); end' newline ...
    'Ts = 1e-4; L1 = 3e-3; R1 = 0.1; Cf = 10e-6;' newline ...
    'diL = (Vin - R1*iL - vC) / L1;' newline ...
    'iL = iL + diL * Ts;' newline ...
    'dvC = iL / Cf;' newline ...
    'vC = vC + dvC * Ts;' newline ...
    'Vout = vC;' newline ...
    'Iabc = iL;' newline ...
    'end']);
add_line(sys, 'Vin/1', 'LCL/1', 'autorouting', 'on');
add_line(sys, 'LCL/1', 'Vout/1', 'autorouting', 'on');
add_line(sys, 'LCL/2', 'Iabc/1', 'autorouting', 'on');
end

function populate_afe(model_name)
sys = [model_name '/AFE_3Level'];
try delete_line(sys, 'In1/1', 'Out1/1'); catch, end
try delete_block([sys '/In1']); catch, end
try delete_block([sys '/Out1']); catch, end

add_block('simulink/Ports & Subsystems/In1', [sys '/Vabc'], 'Position', [30 30 60 50]);
add_block('simulink/Ports & Subsystems/In1', [sys '/PWM'], 'Position', [30 80 60 100]);
add_block('simulink/Ports & Subsystems/Out1', [sys '/Idc'], 'Position', [380 50 410 70]);

add_block('simulink/User-Defined Functions/MATLAB Function', [sys '/AFE_Avg'], ...
    'Position', [120 30 320 100]);
set_ml_fcn(sys, 'AFE_Avg', [...
    'function Idc = fcn(Vabc, PWM)' newline ...
    '%#codegen' newline ...
    '% Average-value model of 3-phase PWM rectifier' newline ...
    '% Idc = sum(duty_k * i_k), i_k from grid voltage / Zeq' newline ...
    'persistent iL' newline ...
    'if isempty(iL), iL = zeros(3,1); end' newline ...
    'Ts = 1e-4; Lac = 5e-3; Rac = 0.1; Vdc_est = 750;' newline ...
    'D = min(max(PWM, 0.05), 0.95);' newline ...
    'Vconv = (D - 0.5) * Vdc_est;' newline ...
    'diL = (Vabc - Vconv - Rac*iL) / Lac;' newline ...
    'iL = iL + diL * Ts;' newline ...
    'Idc = sum(D .* iL);' newline ...
    'end']);
add_line(sys, 'Vabc/1', 'AFE_Avg/1', 'autorouting', 'on');
add_line(sys, 'PWM/1', 'AFE_Avg/2', 'autorouting', 'on');
add_line(sys, 'AFE_Avg/1', 'Idc/1', 'autorouting', 'on');
end

function populate_dc_bus(model_name)
sys = [model_name '/DC_Bus'];
try delete_line(sys, 'In1/1', 'Out1/1'); catch, end
try delete_block([sys '/In1']); catch, end
try delete_block([sys '/Out1']); catch, end

add_block('simulink/Ports & Subsystems/In1', [sys '/Idc_in'], 'Position', [30 40 60 60]);
add_block('simulink/Ports & Subsystems/Out1', [sys '/Vdc'], 'Position', [380 40 410 60]);
add_block('simulink/Ports & Subsystems/Out1', [sys '/Meas'], 'Position', [380 90 410 110]);

add_block('simulink/User-Defined Functions/MATLAB Function', [sys '/CapModel'], ...
    'Position', [120 30 320 110]);
set_ml_fcn(sys, 'CapModel', [...
    'function [Vdc, Meas] = fcn(Idc_in)' newline ...
    '%#codegen' newline ...
    'persistent vc1 vc2' newline ...
    'if isempty(vc1), vc1 = 375; vc2 = 375; end' newline ...
    'Ts = 1e-4; C1 = 4700e-6; C2 = 4700e-6;' newline ...
    '% Split bus capacitor with midpoint' newline ...
    'vc1 = vc1 + (Idc_in * 0.5) * Ts / C1;' newline ...
    'vc2 = vc2 + (Idc_in * 0.5) * Ts / C2;' newline ...
    'Vdc = vc1 + vc2;' newline ...
    'Meas = [Vdc; vc1; vc2];' newline ...
    'end']);
add_line(sys, 'Idc_in/1', 'CapModel/1', 'autorouting', 'on');
add_line(sys, 'CapModel/1', 'Vdc/1', 'autorouting', 'on');
add_line(sys, 'CapModel/2', 'Meas/1', 'autorouting', 'on');
end

function populate_dcdc(model_name)
