function populate_battery(model_name)
sys = [model_name '/Battery_Pack'];
try delete_line(sys, 'In1/1', 'Out1/1'); catch, end
try delete_block([sys '/In1']); catch, end
try delete_block([sys '/Out1']); catch, end

add_block('simulink/Ports & Subsystems/In1', [sys '/Ibatt_in'], 'Position', [30 40 60 60]);
add_block('simulink/Ports & Subsystems/Out1', [sys '/BattMeas'], 'Position', [380 40 410 60]);

add_block('simulink/User-Defined Functions/MATLAB Function', [sys '/BattModel'], ...
    'Position', [120 30 320 80]);
set_ml_fcn(sys, 'BattModel', [...
    'function BattMeas = fcn(Ibatt)' newline ...
    '%#codegen' newline ...
    'persistent soc' newline ...
    'if isempty(soc), soc = 0.3; end' newline ...
    'Ts = 1e-4; CapAh = 100; Ncells = 100; Rint = 0.001;' newline ...
    'soc = soc + Ibatt * Ts / (CapAh * 3600);' newline ...
    'soc = min(max(soc, 0), 1);' newline ...
    'Voc_cell = 3.0 + 1.2*soc;' newline ...
    'Vbatt = Ncells * (Voc_cell + Ibatt * Rint);' newline ...
    'BattMeas = [Vbatt; Ibatt; soc];' newline ...
    'end']);
add_line(sys, 'Ibatt_in/1', 'BattModel/1', 'autorouting', 'on');
