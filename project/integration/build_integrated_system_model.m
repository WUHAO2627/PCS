end

%% ===== MATLAB Function script setter (R2024b compatible) =====
function set_ml_fcn(sys, block_name, code)
% Set the script of a MATLAB Function block via Stateflow API.
% This works in R2024b.
blk_path = [sys '/' block_name];
sf = sfroot();
chart = find(sf, '-isa', 'Stateflow.EMChart', 'Path', blk_path);
if ~isempty(chart)
    chart.Script = code;
end
end
